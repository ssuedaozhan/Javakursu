package Gun15;

public class _02_JavaNestedLoop {
    public static void main(String[] args) {
        // 5 lere kadar carpim tablosunu yap

        for (int i = 0; i <= 5; i++) {

            for (int j = 1; j <=10 ; j++) {
                System.out.println(j+"X"+i+"="+(j*i));
            }

        }
    }
}
