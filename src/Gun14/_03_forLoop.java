package Gun14;

public class _03_forLoop {
    public static void main(String[] args) {
        //0 dan 10 dahil kadar sayilari ekrana yazdiriniz
        // 10 dan 0 dahil kadar sayilari ekrayana yazdirin (ayri bir diger for )

        int i=0;

        for(i=0;i<=10;i++)
            System.out.println("i = " + i);


        for(i=10; i>=0;i--) // i-- gri geri sayar .
            System.out.println("i = " + i);


    }
}
