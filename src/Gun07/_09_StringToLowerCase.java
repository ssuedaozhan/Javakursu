package Gun07;

public class _09_StringToLowerCase {
    public static void main(String[] args) {

        // toLowerCse : stringi kucuk harfe cevirir

        String text = "Merhaba Dunya";

        System.out.println("orjinal hali text = "+ text ); // Merhaba Dunya
        System.out.println("text.toLowerCase()= "+ text.toLowerCase()); // kucultur yani : merhaba dunya

        System.out.println("text= "+ text);// kendisine atama yazmazsak kendisi degismez


    }
}
